//Scrivere un programma che calcoli il massimo in un vettore di dimensione 10 ( valori scelti a piacimento)
//E.S 
// let A:number = [1,5,20,15,500,230];
// Output : 500


let A:number [] = [];
let n : number = 10;
let max : number = 0;
for (let i = 0 ; i<n ; i++){
   let ins = Number (prompt ("inserisci i numeri della lista MAX 10"));
    A.push(ins)
    if (A[i]>max){
        max = A[i]
    }
}
console.log (A);
console.log ("Il numbero più grande è " + max);


